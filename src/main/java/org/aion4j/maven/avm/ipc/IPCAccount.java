package org.aion4j.maven.avm.ipc;

import org.aion4j.avm.helper.crypto.Account;

import java.math.BigInteger;

/**
 * This class is used to store the result from Account faucet goal to the file-system, so that the caller (for example: IDE) can read the result from filesystem.
 */
public class IPCAccount {
    private String privateKey;
    private String address;
    private BigInteger balance;

    public IPCAccount(Account account) {
        if(account != null) {
            privateKey = account.getPrivateKey();
            address = account.getAddress();
        }
    }

    public String getPrivateKey() {
        return privateKey;
    }

    public void setPrivateKey(String privateKey) {
        this.privateKey = privateKey;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public BigInteger getBalance() {
        return balance;
    }

    public void setBalance(BigInteger balance) {
        this.balance = balance;
    }
}
